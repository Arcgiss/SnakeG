public enum Direction {
    RIGHT,
    LEFT,
    UP,
    DOWN;
    
    public static Direction getRIGHT() {
        return RIGHT;
    }

    public static Direction getLEFT() {
        return LEFT;
    }

    public static Direction getUP() {
        return UP;
    }

    public static Direction getDOWN() {
        return DOWN;
    }
}
